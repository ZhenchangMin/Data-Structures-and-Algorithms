#include <iostream>
#include <vector>
using namespace std;
using ull = unsigned long long;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while(T--){
        int n;
        cin >> n;
        vector<ull> a(n);
        for(int i = 0; i < n; i++){
            cin >> a[i];
        }
    }
    return 0;
}